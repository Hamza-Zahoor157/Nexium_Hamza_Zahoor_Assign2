export function translateToUrdu(text: string): string {
  const summarized = text.split(' ').slice(0, 50).join(' ');
  
  return summarized
    .split(/\s+/)
    .map(word => {
      const cleanWord = word.replace(/[^a-zA-Z]/g, '').toLowerCase();
      return dictionary[cleanWord] || word;
    })
    .join(' ');
}